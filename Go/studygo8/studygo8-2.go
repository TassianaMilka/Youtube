package main // Ponto de entrada

import "fmt" // Importando o pacote fmt para impressão

func main() { // Função principal do programa

    // Declaração de variável 
    var i int 

    // Loop(Estrutura de repetição)
    for i = 0; i < 2; i++ {
        // Imprimir a mensagem no console(print)
        fmt.Println("Estudando Linguagem de Programação Go!")
        fmt.Println(i)
     
    }
}
